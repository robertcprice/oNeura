const ALL_SPECIES: [BotanicalSpeciesProfile; 11] = [
    ARABIDOPSIS_THALIANA,
    MALUS_DOMESTICA,
    PYRUS_COMMUNIS,
    PRUNUS_PERSICA,
    PRUNUS_AVIUM,
    CITRUS_SINENSIS,
    CITRUS_X_LIMON,
    BRACHYPODIUM_DISTACHYON,
    LEMNA_MINOR,
    CERATOPHYLLUM_DEMERSUM,
    ELODEA_CANADENSIS,
];

pub fn botanical_species_profiles() -> &'static [BotanicalSpeciesProfile] {
    &ALL_SPECIES
}

pub fn species_profile_by_taxonomy(taxonomy_id: u32) -> Option<BotanicalSpeciesProfile> {
    ALL_SPECIES
        .iter()
        .copied()
        .find(|profile| profile.taxonomy_id == taxonomy_id)
}

pub fn sample_species_profile<R: Rng + ?Sized>(rng: &mut R) -> BotanicalSpeciesProfile {
    let idx = rng.gen_range(0..ALL_SPECIES.len());
    ALL_SPECIES[idx]
}
