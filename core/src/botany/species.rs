#[derive(Debug, Clone, Copy, Serialize, Deserialize, PartialEq, Eq)]
pub enum BotanicalGrowthForm {
    RosetteHerb,
    OrchardTree,
    StoneFruitTree,
    CitrusTree,
    GrassClump,
    FloatingAquatic,
    SubmergedAquatic,
}

#[derive(Debug, Clone, Copy)]
